"""
物理与数学常量模块

本模块定义了 GNSS 数据处理中使用的核心物理常量、坐标系统参数、
以及信号处理相关的预计算值。所有常量均基于 GPS 系统和 WGS84 椭球模型。

主要常量类别：
- GPS 信号频率 (L1, L2)
- 光速与地球半径
- WGS84 椭球参数
- TEC 转换因子
- 观测类型优先级列表
"""

from __future__ import annotations

# ===========================================================================
# GPS 信号频率 (Hz)
# ===========================================================================
# GPS L1 载波频率: 1575.42 MHz
GPS_L1_HZ = 1575.42e6
# GPS L2 载波频率: 1227.60 MHz
GPS_L2_HZ = 1227.60e6

# ===========================================================================
# 基本物理常量
# ===========================================================================
# 真空中光速 (m/s)
LIGHT_SPEED_MPS = 299792458.0
# 地球平均半径 (km)，用于单层电离层映射函数
EARTH_RADIUS_KM = 6371.0

# ===========================================================================
# 时间系统
# ===========================================================================
# GPS 周起点 (ISO 8601 格式): 1980年1月6日 00:00:00 UTC
GPS_EPOCH_ISO = "1980-01-06T00:00:00Z"

# ===========================================================================
# WGS84 参考椭球参数
# ===========================================================================
# 长半轴 (m)
WGS84_A_M = 6378137.0
# 扁率倒数
WGS84_F = 1 / 298.257223563

# ===========================================================================
# 预计算信号波长 (m)
# ===========================================================================
# L1 波长
GPS_L1_LAMBDA_M = LIGHT_SPEED_MPS / GPS_L1_HZ
# L2 波长
GPS_L2_LAMBDA_M = LIGHT_SPEED_MPS / GPS_L2_HZ

# ===========================================================================
# TEC 转换因子
# ===========================================================================
# GPS 双频相位组合的 TEC 转换因子
# 公式: (f1^2 * f2^2) / (40.3 * (f1^2 - f2^2)) / 1e16
# 用于将相位观测值 (cycles) 转换为 TECU (1 TECU = 10^16 electrons/m^2)
GPS_TEC_FACTOR = (
    (GPS_L1_HZ**2 * GPS_L2_HZ**2) / (40.3 * (GPS_L1_HZ**2 - GPS_L2_HZ**2)) / 1e16
)

# DCB (差分码偏差) 从纳秒转换为 TECU 的因子
# 公式: (c * 1e-9) / (40.3e16 * (1/f2^2 - 1/f1^2))
GPS_DCB_NS_TO_TECU = (LIGHT_SPEED_MPS * 1e-9) / (
    40.3e16 * (1 / GPS_L2_HZ**2 - 1 / GPS_L1_HZ**2)
)

# ===========================================================================
# 质量控制阈值
# ===========================================================================
# 低仰角质量标记阈值 (度)，低于此角度的观测被认为质量较低
LOW_ELEVATION_QUALITY_DEG = 20.0

# ===========================================================================
# 观测类型优先级列表
# ===========================================================================
# 双频相位观测类型优先级 (从高到低)
# 优先使用现代信号 (如 L2W, L2X)，回退到传统信号 (L1, L2)
PHASE_PAIR_PRIORITY = [
    ("L1C", "L2W"),  # C/A 码 + 半无码
    ("L1C", "L2X"),  # C/A 码 + 现代民用
    ("L1C", "L2L"),  # C/A 码 + L2C(L)
    ("L1C", "L2S"),  # C/A 码 + L2C(S)
    ("L1W", "L2W"),  # 无码 (W 型)
    ("L1X", "L2X"),  # 现代民用
    ("L1", "L2"),  # 传统 (回退)
]

# 双频码观测类型优先级 (从高到低)
# 用于 STEC 计算和 Hatch 平滑
CODE_PAIR_PRIORITY = [
    ("P1", "P2"),  # 传统 P 码
    ("C1", "P2"),  # C/A + P 码
    ("C1", "C2"),  # 双 C/A
    ("C1C", "C2W"),  # 现代 C/A + 半无码
    ("C1C", "C2X"),  # 现代 C/A + 现代民用
    ("C1W", "C2W"),  # 无码
]
